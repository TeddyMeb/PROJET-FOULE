    private void LancerLaSimulationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LancerLaSimulationActionPerformed
        // TODO add your handling code here:
        int mode = Integer.parseInt((String) Mode.getSelectedItem());
        int longueurRayon = (Integer) LongueurRayon.getValue();
        int nbRayons = (Integer) NombreDeRayons.getValue();
        double lambda = Lambda.getValue()/100.0;
        
        System.out.println("Mode: " + mode);
        System.out.println("Rayon: " + longueurRayon);
        System.out.println("Nb rayons: " + nbRayons);
        System.out.println("Lambda: " + lambda);
        
        simulation.lancerSimulation(mode, lambda, nbRayons, longueurRayon);
    }//GEN-LAST:event_LancerLaSimulationActionPerformed

